absoluto1 :: Flotar ->Flotar
absoluto1 x | x < 0 = -x
            | de lo contrario = x

distanciaManhattan :: (Flotar, Flotar, Flotar) ->(Flotar, Flotar, Flotar) ->Flotar
distanciaManhattan (a, b, c) (d, e, f) = absoluto1 (ad) + absoluto1 (be) + absoluto1 (cf)